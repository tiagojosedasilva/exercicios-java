package aula1;

public class Calculadora {

    public int operando1;
    public int operando2;

    public int adicao(){
        return operando1 + operando2;
    }
    public int subtracao(){
        return operando1 - operando2;
    }
    public int multiplicacao(){
        return operando1 * operando2;
    }
    public int divisao(){
        return operando1 / operando2;
    }
}